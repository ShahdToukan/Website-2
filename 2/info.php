                <p>
                    Focusing on progress and modernity, the Futurists sought to sweep away traditional artistic notions and replace them with an energetic celebration of the machine age. Focus was placed on creating a unique and dynamic vision of the future and artists incorporated portrayals of urban landscapes as well as new technologies such as trains, cars, and airplanes into their depictions. Speed, violence, and the working classes were all glorified by the group as ways to advance change and their work covered a wide variety of artforms, including architecture, sculpture, literature, theatre, music, and even food.
                    Futurism was invented, and predominantly based, in Italy, led by the charismatic poet Marinetti. The group was at its most influential and active between 1909 and 1914 but was re-started by Marinetti after the end of the First World War. This revival attracted new artists and became known as second generation Futurism. Although most prominent in Italy, Futurist ideas were utilized by artists in Britain, the US and Japan and Futurist works were displayed all over Europe. Russian Futurism is usually considered a separate movement, although some Russian Futurism did engage with the earlier Italian movement.
